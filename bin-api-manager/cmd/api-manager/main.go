func main() {

	log := logrus.WithField("func", "main")

	// connect to database
	sqlDB, err := sql.Open("mysql", *dsn)
	if err != nil {
		log.Errorf("Could not access to database. err: %v", err)
		return
	}
	defer sqlDB.Close()

	// connect to cache
	cache := cachehandler.NewHandler(*redisAddr, *redisPassword, *redisDB)
	if err := cache.Connect(); err != nil {
		log.Errorf("Could not connect to cache server. err: %v", err)
		return
	}

	// dbhandler
	db := dbhandler.NewHandler(sqlDB, cache)

	// connect to rabbitmq
	sockHandler := sockhandler.NewSockHandler(sock.TypeRabbitMQ, *rabbitAddr)
	sockHandler.Connect()

	run(sockHandler, db)
}

func init() {
	flag.Parse()

	// init log
	logrus.SetFormatter(joonix.NewFormatter())
	logrus.SetLevel(logrus.DebugLevel)

	// init middleware
	middleware.Init(*jwtKey)

	// init ssl
	if errWrite := writeBase64(constSSLCertFilename, *sslCertBase64); errWrite != nil {
		logrus.Errorf("Could not write the ssl cert file.")
		return
	}

	if errWrite := writeBase64(constSSLPrivFilename, *sslPrivkeyBase64); errWrite != nil {
		logrus.Errorf("Could not write the ssl private key file.")
		return
	}

}

func writeBase64(filename string, data string) error {
	log := logrus.WithFields(logrus.Fields{
		"func":     "writeBase64",
		"filename": filename,
		"data":     data,
	})

	// Create or open the file
	file, err := os.Create(filename)
	if err != nil {
		log.Errorf("Could not create a file. err: %v", err)
		return err
	}
	defer file.Close()

	tmp, err := base64.StdEncoding.DecodeString(data)
	if err != nil {
		log.Fatalf("Error decoding Base64 string: %v", err)
	}

	// Write the string data to the file
	_, err = file.Write(tmp)
	if err != nil {
		log.Errorf("Could not write to file. err: %v", err)
		return err
	}

	return nil
}

func run(
	sockHandler sockhandler.SockHandler,
	db dbhandler.DBHandler,
) {
	log := logrus.WithFields(logrus.Fields{
		"func": "run",
	})

	// create handlers
	requestHandler := requesthandler.NewRequestHandler(sockHandler, "api_manager")
	zmqPubHandler := zmqpubhandler.NewZMQPubHandler()
	websockHandler := websockhandler.NewWebsockHandler(requestHandler)
	serviceHandler := servicehandler.NewServiceHandler(requestHandler, db, websockHandler, *gcpCredential, *gcpProjectID, *gcpBucketName)

	if errSub := runSubscribe(sockHandler, zmqPubHandler); errSub != nil {
		log.Errorf("Could not run subscribe handler. err: %v", errSub)
	}

	runListen(serviceHandler)
}

func runSubscribe(
	rabbitSock rabbitmqhandler.Rabbit,
	zmqHandler zmqpubhandler.ZMQPubHandler,
) error {

	queueNamePod := fmt.Sprintf("%s-%s", commonoutline.QueueNameAPISubscribe, uuid.Must(uuid.NewV4()))

	subscribeTargets := []string{
		string(commonoutline.QueueNameWebhookEvent),
		string(commonoutline.QueueNameAgentEvent),
	}
	subHandler := subscribehandler.NewSubscribeHandler(
		rabbitSock,
		queueNamePod,
		subscribeTargets,

		zmqHandler,
	)

	// run
	if err := subHandler.Run(); err != nil {
		return err
	}

	return nil
}

func runListen(serviceHandler servicehandler.ServiceHandler) {
	log := logrus.WithFields(logrus.Fields{
		"func": "runListen",
	})

	app := gin.Default()

	// swagger
	app.GET("/swagger/*any", ginSwagger.WrapHandler(swaggerFiles.Handler))

	// docs
	app.Static("/docs", "docsdev/build")

	// CORS setting
	// CORS for https://foo.com and https://github.com origins, allowing:
	// - PUT and PATCH methods
	// - Origin header
	// - Credentials share
	// - Preflight requests cached for 12 hours
	app.Use(cors.New(cors.Config{
		AllowOrigins:     []string{"*"},
		AllowMethods:     []string{"POST", "GET", "PUT", "DELETE", "PATCH", "HEAD", "OPTIONS"},
		AllowHeaders:     []string{"Origin", "X-Requested-With", "Content-Type", "Accept", "Authorization"},
		ExposeHeaders:    []string{"Content-Length"},
		AllowCredentials: true,
		MaxAge:           12 * time.Hour,
	}))

	// inject servicehandler
	app.Use(func(c *gin.Context) {
		c.Set(common.OBJServiceHandler, serviceHandler)
		c.Next()
	})

	// set jwt middleware
	app.Use(middleware.JWTMiddleware())

	// apply api router
	api.ApplyRoutes(app)

	logrus.Debug("Starting the api service.")
	if errAppRun := app.RunTLS(":443", constSSLCertFilename, constSSLPrivFilename); errAppRun != nil {
		log.Errorf("The api service ended with error. err: %v", errAppRun)
	}
}
