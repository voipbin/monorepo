var dsn = flag.String("dsn", "testid:testpassword@tcp(127.0.0.1:3306)/test", "database dsn")

var sslKey = flag.String("ssl_private", "./etc/ssl/prikey.pem", "Private key file for ssl connection.")
var sslCert = flag.String("ssl_cert", "./etc/ssl/cert.pem", "Cert key file for ssl connection.")

var jwtKey = flag.String("jwt_key", "voipbin", "key string for jwt hashing")

var rabbitAddr = flag.String("rabbit_addr", "amqp://guest:guest@localhost:5672", "rabbitmq service address.")

// args for redis
var redisAddr = flag.String("redis_addr", "127.0.0.1:6379", "redis address.")
var redisPassword = flag.String("redis_password", "", "redis password")
var redisDB = flag.Int("redis_db", 1, "redis database.")

// @title          VoIPBIN project API
// @version        3.1.0
// @description    RESTful API documents for VoIPBIN project.
// @termsOfService http://swagger.io/terms/

// @contact.name  VoIPBIN Project
// @contact.email pchero21@gmail.com

// @host api.voipbin.net
// @BasePath
func main() {

	log := logrus.WithField("func", "main")

	// connect to database
	sqlDB, err := sql.Open("mysql", *dsn)
	if err != nil {
		log.Errorf("Could not access to database. err: %v", err)
		return
	}
	defer sqlDB.Close()

	// connect to cache
	cache := cachehandler.NewHandler(*redisAddr, *redisPassword, *redisDB)
	if err := cache.Connect(); err != nil {
		log.Errorf("Could not connect to cache server. err: %v", err)
		return
	}

	// dbhandler
	db := dbhandler.NewHandler(sqlDB, cache)

	// connect to rabbitmq
	sock := rabbitmqhandler.NewRabbit(*rabbitAddr)
	sock.Connect()

	run(sock, db)
}

func init() {
	flag.Parse()

	// init log
	logrus.SetFormatter(joonix.NewFormatter())
	logrus.SetLevel(logrus.DebugLevel)

	// init middleware
	middleware.Init(*jwtKey)
}

func run(
	sock rabbitmqhandler.Rabbit,
	db dbhandler.DBHandler,
) {
	log := logrus.WithFields(logrus.Fields{
		"func": "run",
	})

	// create handlers
	requestHandler := requesthandler.NewRequestHandler(sock, "api_manager")
	zmqPubHandler := zmqpubhandler.NewZMQPubHandler()
	websockHandler := websockhandler.NewWebsockHandler(requestHandler)
	serviceHandler := servicehandler.NewServiceHandler(requestHandler, db, websockHandler)

	if errSub := runSubscribe(sock, zmqPubHandler); errSub != nil {
		log.Errorf("Could not run subscribe handler. err: %v", errSub)
	}

	runListen(serviceHandler)
}

func runSubscribe(
	rabbitSock rabbitmqhandler.Rabbit,
	zmqHandler zmqpubhandler.ZMQPubHandler,
) error {

	queuNamePod := fmt.Sprintf("%s-%s", commonoutline.QueueNameAPISubscribe, uuid.Must(uuid.NewV4()))

	subscribeTargets := []string{
		string(commonoutline.QueueNameWebhookEvent),
	}
	subHandler := subscribehandler.NewSubscribeHandler(
		rabbitSock,
		queuNamePod,
		subscribeTargets,

		zmqHandler,
	)

	// run
	if err := subHandler.Run(); err != nil {
		return err
	}

	return nil
}

func runListen(serviceHandler servicehandler.ServiceHandler) {
	log := logrus.WithFields(logrus.Fields{
		"func": "runListen",
	})

	app := gin.Default()

	// swagger
	app.GET("/swagger/*any", ginSwagger.WrapHandler(swaggerFiles.Handler))

	// docs
	app.Static("/docs", "docsdev/build/html")

	// CORS setting
	// CORS for https://foo.com and https://github.com origins, allowing:
	// - PUT and PATCH methods
	// - Origin header
	// - Credentials share
	// - Preflight requests cached for 12 hours
	app.Use(cors.New(cors.Config{
		AllowOrigins:     []string{"*"},
		AllowMethods:     []string{"POST", "GET", "PUT", "DELETE", "PATCH", "HEAD", "OPTIONS"},
		AllowHeaders:     []string{"Origin", "X-Requested-With", "Content-Type", "Accept", "Authorization"},
		ExposeHeaders:    []string{"Content-Length"},
		AllowCredentials: true,
		MaxAge:           12 * time.Hour,
	}))

	// inject servicehandler
	app.Use(func(c *gin.Context) {
		c.Set(common.OBJServiceHandler, serviceHandler)
		c.Next()
	})

	// set jwt middleware
	app.Use(middleware.JWTMiddleware())

	// apply api router
	api.ApplyRoutes(app)

	logrus.Debug("Starting the api service.")
	if errAppRun := app.RunTLS(":443", *sslCert, *sslKey); errAppRun != nil {
		log.Errorf("The api service ended with error. err: %v", errAppRun)
	}
}
